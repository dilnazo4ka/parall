#include <bits/stdc++.h>                 // подключаю стандартные C++ библиотеки
using namespace std;                      // чтобы не писать std::

int main() {                              // точка входа
    const int N = 1'000'000;              // размер массива = 1,000,000
    vector<int> a(N);                     // создаю вектор на N элементов

    mt19937 gen(42);                      // фиксирую seed, чтобы результаты были воспроизводимы
    for (int i = 0; i < N; i++) {         // иду по всему массиву
        a[i] = gen() % 100;               // заполняю случайными числами [0..99]
    }

    cout << "Generated N=" << N << "\n";  // печатаю подтверждение
    cout << "First 10: ";                 // печатаю первые 10 для проверки
    for (int i = 0; i < 10; i++) cout << a[i] << " "; // вывожу 10 элементов
    cout << "\n";                         // перевод строки

    return 0;                             // успешное завершение
}
